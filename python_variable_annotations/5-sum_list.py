#!/usr/bin/env python3
"""
This script defines a function to calculate the sum of elements in a list.
"""


def sum_list(input_list: list = float) -> sum:
    """
    Calculate the sum of elements in a list.

    Args:
        input_list (list): A list of floats.

    Returns:
        float: The sum of elements in the input list.
    """
    return sum(input_list)
